package ejercicio8palindromo;
/*
Se crea la clase nodo con los atributos
"dato" de tipo char y "siguiente" de tipo
nodo.Esta clase nos ayudara a representar 
los nodos de la clases Pila y la 
ListaEnlazadaCircular.
*/
public class Nodo {
    char dato;
    Nodo siguiente;
    public Nodo(char dato) {
        this.dato = dato;
        this.siguiente = null;
    }
}








